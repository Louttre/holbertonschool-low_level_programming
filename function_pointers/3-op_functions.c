#include <stdio.h>
#include "3-calc.h"
/**
 * op_add - calcul addition
 * @a: int
 * @b: int
 * Return: result of a + b
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - calcul substraction
 * @a: int
 * @b: int
 * Return: result of a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - calcul multiplication
 * @a: int
 * @b: int
 * Return: result of a * b
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - calcul division
 * @a: int
 * @b: int
 * Return: result of a / b
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - calcul modulo
 * @a: int
 * @b: b
 * Return: result of a % b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
